function abrirModal(id) {
  const modal = document.getElementById('modal' + id);
  if (modal) {
    modal.style.display = 'block';
  }
}

function fecharModal(id) {
  const modal = document.getElementById('modal' + id);
  if (modal) {
    modal.style.display = 'none';
  }
}

